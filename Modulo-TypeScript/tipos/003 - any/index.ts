// Ele é útil quando você não tem certeza do tipo de dado que será atribuído a uma variável, 
// ou quando você deseja desativar temporariamente a verificação de tipos em uma parte específica do código. 
// No entanto, o uso excessivo do tipo any pode levar a problemas de segurança e manutenção do código, 
// pois ele desativa as vantagens da tipagem estática oferecida pelo TypeScript. 
// Portanto, é recomendado usar o tipo any com cautela e preferir tipos mais específicos sempre que possível.